#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*obj;

	obj = (t_list *) malloc(sizeof(*obj));
	if (!obj)
		return (0);
	obj->content = content;
	obj->next = NULL;
	return (obj);
}
