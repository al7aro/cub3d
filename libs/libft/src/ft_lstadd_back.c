#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*aux;

	if (lst != NULL)
	{
		aux = (*lst);
		if (aux != NULL)
		{
			while (aux != NULL && aux->next != NULL)
				aux = aux->next;
			aux->next = new;
		}
		else
			*lst = new;
	}
}
